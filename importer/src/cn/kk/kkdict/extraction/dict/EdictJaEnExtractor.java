    public static final String OUT_DIR = Configuration.IMPORTER_FOLDER_EXTRACTED_DICTS.getPath(Source.DICT_EDICT);

    public static final String[] IRRELEVANT_WORDS_STRINGS = {};

    public static void main(String args[]) throws IOException {
        Map<String, EdictCategory> categoriesMap = new HashMap<String, EdictCategory>();
        final JECategory[] values = JECategory.values();
        for (int i = 0; i < values.length; i++) {
            JECategory c = values[i];
            categoriesMap.put(c.getConstantName(), c);
        }
        new EdictJaEnExtractor().extractDict(TranslationSource.EDICT_JA_EN, EDICT_FILE, Helper.CHARSET_UTF8, OUT_DIR,
                Language.JA, Language.EN, categoriesMap, IRRELEVANT_WORDS_STRINGS);
    }

    @Override
    protected String readName(final String line) {
        final int bIdx = line.indexOf('[');
        if (bIdx != -1) {
            return line.substring(0, bIdx - 1);
        } else {
            final int dIdx = line.indexOf('/');
            if (dIdx != -1) {
                return line.substring(0, dIdx - 1);
            } else {
                return null;
            }
        }
    }

